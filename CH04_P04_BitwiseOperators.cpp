

#include<iostream>
using namespace std;
int main()
{
	int a=9,b=2,c;

	/**     Bitwise AND &
	 *  		8 4 2 1 
	 * ---------------------
	 * 		  9:1 0 0 1
	 *        2:0 0 1 0
	 * 		-------------
	 * 		  &:0 0 0 0 => 0
	 * */
	c= a&b;
	cout<<a<<" & "<<b<<" = "<<c<<endl;

	
	/************************************************************ 
	 *    Bitwise OR |
	 *  		8 4 2 1 
	 * ---------------------
	 * 		  9:1 0 0 1
	 *        2:0 0 1 0
	 * 		-------------
	 * 		  |:1 0 1 1 => 11
	 * */
	c= a|b;
	cout<<a<<" | "<<b<<" = "<<c<<endl;

	/*************************************************************** 
	 *    Bitwise Ex-OR ^ : both are same then '0' or '1'
	 *  		8 4 2 1 
	 * ---------------------
	 * 		  9:1 0 0 1
	 *        2:0 0 1 0
	 * 		-------------
	 * 		 ^ :1 0 1 1 => 11  
	 * */
	c= a^b;
	cout<<a<<" ^ "<<b<<" = "<<c<<endl;

	/******************************************************************** 
	 *     Bitwise negation ~
 	/*
 	*	 c = -(a+1)
 	*      = -(9+1)
 	*      = -10
 	* 
 	* 
	 * */
	c= ~a;
	cout<<" ~ "<<a<<" = "<<c<<endl;

	/***********************************************************
	 * 	Bitwise Left shift <<
	 * 	
	 * c = a*2^b
	 *   = 9*2^2
	 *   = 9*4
	 *   = 36
	 * 
	 * */

	c= a<<b;
	cout<<a<<" << "<<b<<" = "<<c<<endl;

	/***********************************************************************
	 * 	Bitwise Right shift <<
	 * 	
	 * c = a/2^b
	 *   = 9/2^2
	 *   = 9/4
	 *   = 2.25
	 * 
	 * */

	c= a>>b;
	cout<<a<<" >> "<<b<<" = "<<c<<endl;

}